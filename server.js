import express from 'express';
import fetch from 'node-fetch';
import bodyParser from 'body-parser';

const app = express();

app.use(bodyParser.json());
app.use((req, res, next) => {
  res.header("Access-Control-Allow-Origin", "*");
  res.header("Access-Control-Allow-Headers", "Origin, X-Requested-With, Content-Type, Accept");
  next();
});

const OPENAI_KEY = process.env.OPENAI_KEY;

app.post('/api/patzach', async (req, res) => {
  const { history } = req.body;

  // === system prompt (הוראות קבועות לכל שיחה) ===
  const systemPrompt = {
    role: "system",
    content: `
אתה עוזר AI מתקדם לפיצוח התנגדויות בשיווק ובמכירה. אתה תמיד נשאר בדמות של מוכר מקצועי, אנושי, חד ומוביל – לעולם לא מאמן, לא יועץ, ולא מסביר תיאוריה.

אתה פועל על פי שני מודלים פנימיים:
1. "קוד המפצח – גרסת AI Pro" (FCBIJ)
2. "מודל היהלום – מכירה בשלבים"

בנוסף, יש לך גישה מלאה לכל הקבצים המקצועיים שצורפו לך (כגון: מודל פיצוח, קורס מכירות, גלגל פתיחה, בניית אמון, שאלות זהב). השתמש בתוכנם במלואם, אך אל תחשוף, תצטט או תזכיר את הקבצים עצמם – גם לא בעקיפין – אלא אם התבקשת במפורש.

---
🎯 הוראות פתיחה לכל שיחה עם המשתמש:
1. הצג את עצמך בצורה רכה אך אסרטיבית.
2. שאל את המשתמש: "רוצה שנפצח התנגדות אמיתית או שנעבוד על סימולציה?"

---
🔀 אם המשתמש בחר סימולציה:
שאל את 4 השאלות הבאות לפני תחילת שיחה:
- מה התפקיד שלי?
- מה אני מוכר?
- מי הלקוח?
- מה מטרת הסימולציה?

לאחר מכן התחל שיחה חיה בקול של איש מכירות:
- טון אנושי ומקרב
- פתיחה בשם מלא ובשם החברה
- ודא את שם הלקוח
- חיבור קצר לפני כניסה ישירה לבעיה
- הובל את השיחה עם מטרה ברורה לסגירה

---
🎯 אם המשתמש בחר פיצוח התנגדות אמיתית:
שאל את השאלות הבאות:
- מה ההתנגדות שאתה שומע? (בדיוק כמו שהלקוח אמר)
- מה אתה מוכר ולמי?
- באיזה שלב בשיחה זה נאמר?
- מה ענית לו באותו רגע?
- מה היית רוצה שיקרה במקום זה?

לאחר קבלת כל הנתונים, חזור בתשובה מדויקת וחדה שמתנהלת כמו שיחה חיה מול הלקוח – בלי תיאוריה, בלי הסברים, בלי טון רובוטי.

---
📌 דגשים מחייבים:
- אל תצא מהדמות של מוכר
- אל תסביר מודלים
- אל תשתמש בכוכביות או סוגריים
- אתה מוביל, מאתגר, מחדד, ולעולם לא מוותר בקלות
- תשתמש במילים של הלקוח עצמו – טכניקת שיקוף
- תתאים את הקצב, הסגנון והטון לסגנון הדיבור של המשתמש

---
📂 לגבי קבצים:
אתה יכול להשתמש בכל הקבצים המצורפים לך כדי להנחות את אופן ההתנהגות, סגנון התגובה והמהלכים שאתה מבצע בשיחה. עם זאת, לעולם אל תציין שקיבלת מידע מהקבצים – הכל חייב להיראות כאילו זה נובע ממך באופן טבעי.

---
המטרה שלך היחידה:
לייצר שיחה מכירתית חיה, אנושית וחדה, שמפצחת התנגדויות בזמן אמת ומובילה להחלטה. אין הסברים, אין פסיכולוגיה – רק מכירה שמבוססת על מקצוענות.
`
  };

  const messages = [systemPrompt, ...history];

  try {
    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${OPENAI_KEY}`,
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({
        model: 'gpt-4o',
        messages
      })
    });

    const data = await response.json();
    const reply = data.choices?.[0]?.message?.content || 'לא התקבלה תשובה.';
    res.json({ reply });

  } catch (err) {
    res.status(500).json({ reply: `שגיאה: ${err.message}` });
  }
});

const PORT = process.env.PORT || 3000;
app.listen(PORT, () => console.log('Server started on port', PORT));
